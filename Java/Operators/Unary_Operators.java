public class Unary_Operators {
    public static void main(String[] ags) {
        int num1 = 10;
        System.out.println("Value before increement: " + num1);
        System.out.println("Value after increement: " + ++num1);
        System.out.println();
        System.out.println("Value before decreement: " + num1);
        System.out.println("Value after decreement: " + --num1);
    }
}
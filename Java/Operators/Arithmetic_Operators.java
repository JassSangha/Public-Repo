public class Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 10;
        float b = 5.0f;
        System.out.println("A + B: " + (a + b));
        System.out.println("A - B: " + (a - b));
        System.out.println("A * B: " + (a * b));
        System.out.println("A / B: " + (a / b));/*
                                                 * Result of division always comes in floating point if either one of
                                                 * the number is a float
                                                 */
        System.out.println("A % B: " + (a % b));
    }
}

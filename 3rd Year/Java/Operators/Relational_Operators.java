public class Relational_Operators {
    public static void main(String[] args) {
        int a = 10;
        float b = 5.0f;
        System.out.println("A: " + (a) + "B: " + (b));
        System.out.println("A == B: " + (a == b));
        System.out.println("A != B: " + (a != b));
        System.out.println("A < B: " + (a < b));
        System.out.println("A > B: " + (a > b));
        System.out.println("A <= B: " + (a <= b));
        System.out.println("A >=B: " + (a >= b));
    }
}
